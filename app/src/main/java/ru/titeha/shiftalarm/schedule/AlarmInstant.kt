package ru.titeha.shiftalarm.schedule

import java.time.Instant
import java.time.LocalDateTime
import java.time.ZoneId

/**
 * Перевод локального времени будильника в момент времени ([Instant]) на границе AlarmManager.
 *
 * Будильник задаётся в ЛОКАЛЬНОМ календарном времени («каждый день в 07:00»), поэтому весь расчёт
 * идёт в `LocalDate`/`LocalTime`/`LocalDateTime`, а в абсолютный момент переводим только здесь — по
 * часовому поясу устройства. При смене пояса будильники перепланируются (`TIMEZONE_CHANGED`), то есть
 * следуем локальному времени устройства, а не «замороженному» моменту.
 *
 * Политика переходов летнего времени (DST), когда локальное время неоднозначно:
 * - **Пробел** (перевод часов вперёд, локального времени не существует, напр. 02:30) — момент
 *   сдвигается ВПЕРЁД на длину пробела по правилам зоны. Будильник не «пропадает», а звонит сразу
 *   после перевода.
 * - **Наложение** (перевод часов назад, локальное время встречается дважды, напр. 02:30) — берётся
 *   ПЕРВОЕ вхождение (более ранний offset), чтобы один будильник не прозвонил дважды.
 *
 * Обе политики уже реализует [LocalDateTime.atZone] (gap → сдвиг вперёд, overlap → ранний offset).
 * Этот объект фиксирует их как явное поведение и покрыт unit-тестами, чтобы правило не «уплыло».
 */
object AlarmInstant {

  /** Момент времени для локального [local] в зоне [zone] по политике DST выше. */
  fun of(local: LocalDateTime, zone: ZoneId): Instant =
    local.atZone(zone).toInstant()

  /** Как [of], но сразу в epoch millis для AlarmManager. Зона по умолчанию — пояс устройства. */
  fun epochMilli(local: LocalDateTime, zone: ZoneId = ZoneId.systemDefault()): Long =
    of(local, zone).toEpochMilli()
}

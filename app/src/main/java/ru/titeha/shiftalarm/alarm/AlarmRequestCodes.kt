package ru.titeha.shiftalarm.alarm

/**
 * requestCode для PendingIntent'ов будильника, выведённые из id.
 *
 * Вынесено за шов (чистый объект без Android), чтобы инвариант можно было проверить unit-тестом.
 *
 * Инвариант: для каждого id — РАЗНЫЕ коды на «сработать» ([fire]) и «показать экран» ([show]), и коды
 * не пересекаются между разными будильниками. Схема «`2*id` (чётный) / `2*id+1` (нечётный)» это
 * гарантирует в пределах диапазона id, укладывающегося в Int.
 *
 * Long→Int: формально при `id ≥ 2^31` возможна коллизия, но id Room — последовательные с 1, поэтому
 * реально это недостижимо (нужно ~2 млрд будильников). Формулу СПЕЦИАЛЬНО не меняем на hash: смена
 * значения requestCode осиротит уже поставленные PendingIntent'ы (их не отменить старым кодом → риск
 * двойного срабатывания). Тест фиксирует инвариант, а не конкретные числа.
 */
object AlarmRequestCodes {
    /** requestCode срабатывания (широковещательный AlarmReceiver). */
    fun fire(id: Long): Int = (id * 2).toInt()

    /** requestCode показа экрана «Стоп» (full-screen PendingIntent). */
    fun show(id: Long): Int = (id * 2 + 1).toInt()

    /**
     * requestCode снуз-звонка — ОТДЕЛЬНЫЙ неймспейс, чтобы снуз-PendingIntent не затирал базовое
     * расписание. Базовые [fire]/[show] неотрицательные (id ≥ 1), поэтому снуз уводим в отрицательный
     * диапазон: он гарантированно дизъюнктен с fire/show, а разные id дают разные коды.
     */
    fun snooze(id: Long): Int = -(id * 2 + 2).toInt()
}
